        // dd($request->all());


        category::create([
           'name' => $request->category_name,
           'slug' => Str::slug($request->category_name),
           'is_active' => $request->filled('is_active'),
        ]);
        Session::flash('status','category created a sucessfully!');
        return back();
    }

